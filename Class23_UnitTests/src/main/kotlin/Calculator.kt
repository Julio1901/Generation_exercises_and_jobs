import kotlin.math.*

class Calculator {

    fun sum(firstNum : Int , secondNum : Int) = firstNum + secondNum

    fun subtration(firstNum : Int , secondNum : Int) = firstNum - secondNum

    fun  division(firstNum : Int , secondNum : Int) = firstNum / secondNum

    fun multiplication(firstNum : Int , secondNum : Int) = firstNum * secondNum

    fun power(firstNum : Double , secondNum : Int) = firstNum.pow(secondNum)

    fun squadRoot(num : Double) = sqrt(num)

}